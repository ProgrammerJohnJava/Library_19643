    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTextFieldTITLE1.getText().isEmpty() || jTextFieldTITLE1.getText() == null) {
            JOptionPane.showMessageDialog(rootPane, "Fill in The Title");
            jTextFieldTITLE1.requestFocus();
        } else {
            Book u = new BookDao().findByTitle(jTextFieldTITLE1.getText());
            SearchedBook = u;
            if (u == null) {
                JOptionPane.showMessageDialog(rootPane, "Not Found");
                jTextFieldTITLE1.setText("");
                jTextFieldTITLE1.requestFocus();
            } else {
                jTextFieldTITLE1.setText(u.getTitle());
                jTextFieldPHOUSE1.setText(u.getPublishingHouse());
                jTextFieldAUTHOR1.setText(u.getAuthor());
                jTextFieldPAGES1.setText(u.getPages() + "");
                jComboBoxCATEGORY1.setSelectedItem(u.getBookCategory());
                jDateChooser2.setDate(u.getDateOfPublication());
            }

        }
    }//GEN-LAST:event_jButton3ActionPerformed

