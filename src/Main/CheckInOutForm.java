    public void viewBookBorrowed() {
        List<CheckInOut> list = new CheckInOutDao().findAll(CheckInOut.class);
        String[] title = {"#", "Title", "Publish House", "Category Book","First Name","Last Name", "Client Category", "Status", "Operation Category", "Borrow Date", "Return Date"};
        Object[][] data = new Object[list.size()][title.length];
        int i = 0;

        for (CheckInOut u : list) {
            data[i][0] = i + 1;
            data[i][1] = u.getBook().getTitle();
            data[i][2] = u.getBook().getPublishingHouse();
            data[i][3] = u.getBook().getBookCategory().getCategoryName();
            data[i][4] = u.getClient().getFirstName();
            data[i][5] =  u.getClient().getLastName();
            data[i][6] = u.getStatus();
            data[i][7] = u.getOperationCategory();
            data[i][8] = new SimpleDateFormat("dd/MMM/yyyy").format(u.getBorrowDate());
            data[i][9] = new SimpleDateFormat("dd/MMM/yyyy").format(u.getReturnedDate());
            i++;
        }
        userTable.setModel(new DefaultTableModel(data, title));
        userTable1.setModel(new DefaultTableModel(data, title));

    }

